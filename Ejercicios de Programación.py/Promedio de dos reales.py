# --- Ejercicio 4: Promedio de dos reales ---
# [Extraído de la fuente 9]
print("--- Ejercicio 4 ---")
# Pedimos el primer número real
real1 = float(input("4. Introduce el primer número real: "))
# Pedimos el segundo número real
real2 = float(input("   Introduce el segundo número real: "))
# Calculamos el promedio (suma dividida por 2)
promedio = (real1 + real2) / 2
print(f"El promedio de {real1} y {real2} es {promedio}\n")