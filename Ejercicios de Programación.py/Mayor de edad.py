# --- Ejercicio 5: Mayor de edad (Booleano) ---
# [Extraído de la fuente 10]
print("--- Ejercicio 5 ---")
# Pedimos la edad como un entero
edad = int(input("5. Introduce tu edad: "))
# La comparación (>=) produce un resultado booleano (True o False)
es_mayor_de_edad = edad >= 18
print(f"¿Es mayor de edad? {es_mayor_de_edad}\n")